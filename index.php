        window.toggleAI = function() {
            const sector = document.getElementById('ai-command-sector');
            sector.style.display = sector.style.display === "none" ? "flex" : "none";
        };

        window.askAI = async function() {
            const input = document.getElementById('master-input');
            const feed = document.getElementById('discussion-feed');
            const wave = document.getElementById('thinking-wave');
            const query = input.value.trim();
            
            if(!query) return;

            feed.innerHTML += `<div class="user-msg">${query}</div>`;
            input.value = '';
            feed.scrollTop = feed.scrollHeight;
            wave.style.display = "flex";

            try {
                const result = await chat.sendMessage(query);
                const response = await result.response;
                wave.style.display = "none";
                
                const msgDiv = document.createElement('div');
                msgDiv.className = 'ai-msg';
                feed.appendChild(msgDiv);
                
                // Simple typewriter effect
                let text = response.text();
                let i = 0;
                function type() {
                    if (i < text.length) {
                        msgDiv.innerHTML += text.charAt(i);
                        i++;
                        feed.scrollTop = feed.scrollHeight;
                        setTimeout(type, 10);
                    }
                }
                type();
            } catch (err) {
                wave.style.display = "none";
                feed.innerHTML += `<div class="ai-msg" style="color:red">Neural link failure. Check connectivity.</div>`;
            }
        };

        document.getElementById('master-input').addEventListener('keypress', (e) => { if(e.key === 'Enter') askAI(); });
